legismod.jags <- function()  {
     for (i in 1:100){
         for(j in 1:486){
             y[i,j] ~ dbern(pi[i,j])
             logit(pi[i,j]) <- x[i]*beta[j,1] - beta[j,2]
             }
         }
         
     helms <- x[66];          ## monitor these nodes as sanity checks
     kennedy <- x[41];  
     fiengold <- x[97]; 
     boxer <- x[9];     
     chafee <- x[77];   
     mosely <- x[26];  
     gramm <- x[85];   
 
     ## priors        
     for (i in 1:40){ x[i] ~ dnorm(0,1) }
     for (i in 42:65){ x[i] ~ dnorm(0,1) }
     for (i in 67:100){ x[i] ~ dnorm(0,1) }
     x[41] <- -1.0  ## Kennedy on the left
     x[66] <- 1.0   ## Helms on the right
     
     for (j in 1:486){
       beta[j,1:2] ~ dmnorm(b0[1:2],B0[1:2,1:2]);
     }
    b0[1] <- 0;     b0[2] <- 0;
    B0[1,1] <- .16; B0[2,2] <- .16; 
    B0[1,2] <- 0;     B0[2,1] <- 0;
  }    	



